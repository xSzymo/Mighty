package game.mightywarriors.other.exceptions;

public class NotProperlyChampionsException extends Exception {

    public NotProperlyChampionsException() {
        super();
    }

    public NotProperlyChampionsException(String message) {
        super(message);
    }

    public NotProperlyChampionsException(String message, Throwable cause) {
        super(message, cause);
    }

    public NotProperlyChampionsException(Throwable cause) {
        super(cause);
    }
}
